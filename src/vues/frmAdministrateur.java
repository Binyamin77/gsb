    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        fm = new FonctionsMetier();
        
        mdlRegion = new ModelRegion();
        mdlRegion.LoadDatasRegion(fm.getAllRegion());
        
        mdlSecteur = new ModelSecteur();
        mdlSecteur.LoadDatasSecteur(fm.getAllSecteur());
        
        mdlTravailler = new ModelTravailler();
        mdlTravailler.LoadDatasTravailler(fm.getAllTravailler());
        
        mdlVisiteur = new ModelVisiteur();
        mdlVisiteur.LoadDatasVisiteur(fm.getAllVisiteur());
        
        mdlLabo = new ModelLabo();
        mdlLabo.LoadDatasLabo(fm.getAllLabo());
        

        
        tblSecteur.setModel((TableModel) mdlSecteur);
        tblRegion.setModel((TableModel) mdlRegion);
        tblTravailler.setModel((TableModel) mdlTravailler);
        tblVisiteur.setModel((TableModel) mdlVisiteur);
        tblLabo.setModel((TableModel) mdlLabo);
        
        
        
    }//GEN-LAST:event_formWindowOpened

