    private void tblLaboMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLaboMouseClicked
        int numUser = Integer.parseInt(tblLabo.getValueAt(tblLabo.getSelectedRow(),0).toString());
        fm = new FonctionsMetier();
        ModelTicket mdlTic = new ModelTicket();
        
        mdlTic.LoadDats2Colonnes(fm.getAllTicketsByIdUser(numUser));
        
        tblTravailler.setModel(mdlTic);
        
    }//GEN-LAST:event_tblLaboMouseClicked

