        fm = new FonctionsMetier();
        int IdLabo = Integer.parseInt(tblLabo.getValueAt(tblLabo.getSelectedRow(), 0).toString());;
        fm.ModifLabo(IdLabo,nomChefM.getText(), nomLaboM.getText());
