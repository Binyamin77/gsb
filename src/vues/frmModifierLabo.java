    private void Actualisation()
    {
        
        ConnexionBdd cnx = new ConnexionBdd();
        fm = new FonctionsMetier();
        ModelLabo reg = new ModelLabo();
        
        reg.LoadDatasLabo(fm.getAllLabo());
        tblLabo.setModel(reg);
        
    
    }
